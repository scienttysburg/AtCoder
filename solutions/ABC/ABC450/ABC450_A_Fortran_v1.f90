program ABC450_A_Fortran_v1
  ! ============================================================================
  implicit none
  ! --- Input Data -------------------------------------------------------------
  integer(8) :: N
  ! --- Loop Counters ----------------------------------------------------------
  integer(8) :: i
  ! ============================================================================

  ! === Input ==================================================================
  read(*,*) N

  ! === Results ================================================================
  do i = N, 1_8, -1_8
    if(i > 1_8)then
      write(*,'(I0,A)',advance='no') i, ','
    else
      write(*,'(I0)') i
    end if
  end do

! ==============================================================================
end program ABC450_A_Fortran_v1